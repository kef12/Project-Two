module.exports = function(sequelize, DataTypes) {
  var Books = sequelize.define("Books", {
    title: {
      type: DataTypes.STRING,
      allowNull: false,
      validate: {
        len: [1]
      }
    },
    body: {
      type: DataTypes.TEXT,
      allowNull: false,
      len: [1]
    }
  });

  return Books;
};
